		<p>Negli ultimi anni si sono fatte sempre crescenti le richieste
			estetiche: oltre che la bocca sana si vuole che i denti siano belli,
			allineati, bianchi: un sorriso è il primo e più gradito biglietto da
			visita. Pur esistendo in commercio kit per lo sbiancamento dei denti
			da effettuare a casa, il sistema più sicuro e veloce è lo
			Sbiancamento ambulatoriale, che viene praticato presso lo studio
			dentistico: un sistema sbiancante costituito da una soluzione di
			perossido di idrogeno al 35% che viene miscelata in ambulatorio con
			sodio perborato e pigmentata di rosa con estratti di cocciniglia (un
			colorante fotoassorbente), viene attivato da una luce, meglio se
			laser, che avvia lo schiarimento/sbiancamento dei denti (reazione di
			ossidazione). Le gengive vengono protette da un gel. Il trattamento
			richiede circa mezz’ora per arcata. Prima di intraprendere una
			terapia sbiancante bisogna tener presenti alcuni importanti aspetti:
			innanzitutto, almeno tre giorni prima dello sbiancamento, la bocca
			deve essere sottoposta ad una seduta di igiene ambulatoriale nel
			corso della quale verrà effettuata una visita per evidenziare le
			eventuali carie da curare prima dello sbiancamento, e comunque tutte
			quelle che sono le controindicazioni alla terapia, come
			ipersensibilità dentinale, superfici radicolari esposte, gravi
			perdite di smalto o fratture dello smalto stesso, o situazioni con un
			tale grado di pigmentazione da rendere vano qualsiasi tentativo di
			sbiancamento. La presenza di otturazioni o di protesi fisse, anche in
			ceramica, comporta dei problemi in quanto lo sbiancante non ha nessun
			effetto su questi materiali. In questi casi, per ottenere una buona
			estetica, bisogna ricorrere alle faccette o agli elementi protesici
			in ceramica.</p>
