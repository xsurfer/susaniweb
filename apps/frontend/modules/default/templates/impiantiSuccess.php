		<p>Le controindicazioni alla terapia implantologia sono quelle comuni
			a tutte le procedure chirurgiche: cure antitumorali eseguite
			recentemente, malattie in atto, gravidanza in corso, stati di
			tossicodipendenza. Il fumo e la scarsa igiene orale facilitano le
			complicazioni. Sarà buona norma avvertire l'odontoiatra se si
			assumono abitualmente farmaci, in modo che la terapia possa essere
			aggiustata, se necessario, nei giorni immediatamente precedenti
			l'intervento. Le complicanze riguardano il 4-5% dei casi, nei quali
			non avviene l'osteointegrazione e l'impianto viene espulso. Ciò
			capita generalmente nei due mesi successivi all'intervento ed è un
			processo indolore. Dopo due o tre settimane si può procedere al
			posizionamento in quella sede di un nuovo impianto. Sono molto rare
			le perdite di impianti negli anni successivi alla protesizzazione,
			anche perchè al momento di applicare le capsule su di essi se ne
			verifica la stabilità e la buona salute.</p>

