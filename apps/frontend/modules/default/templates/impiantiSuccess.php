		<p>Il successo della terapia dipende in modo decisivo dalla
			collaborazione del paziente. Sarà necessario effettuare una
			radiografia panoramica delle arcate dentarie e, in alcuni casi, una
			TAC maxillo-facciale col metodo Dentascan. In casi particolari è
			possibile ricavare, da una apposita elaborazione della TAC, una
			mascherina in plastica che ci permetterà di inserire gli impianti più
			sicuramente ed in maniera meno 'cruenta'. Sarà necessario assumere
			una dose di antibiotico e una di antinfiammatorio per sei giorni,
			iniziando da quello precedente l'intervento. Nella prima settimana
			dopo l'impianto è preferibile non masticare sulla ferita e pulire
			regolarmente con lo spazzolino gli altri denti evitando di
			traumatizzare la zona operata. Continuare così anche nelle settimane
			successive, curando di avvertire tempestivamente l'odontoiatra nel
			caso si noti una qualsiasi alterazione nella zona, o si percepisca
			dolore o pulsazione. Dopo la protesizzazione sarà necessario pulire
			regolarmente i denti con spazzolino e dentifricio dopo ogni pasto e,
			almeno una volta al giorno, usare il filo interdentale o gli
			scovolini dritti per evitare l'accumulo di placca sugli elementi di
			ponte e sui colletti degli impianti, sciacquando poi la bocca con
			collutorio. Il rispetto di queste semplici norme è garanzia per
			mantenere in buona salute le gengive e quindi gli impianti per lungo
			tempo. La durata del lavoro dipenderà dall'applicazione e
			dall'impegno quotidiano nell'igiene orale.</p>
