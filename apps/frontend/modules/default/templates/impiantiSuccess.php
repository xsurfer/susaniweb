	<div class="content">
		<p>La sostituzione dei denti persi comporta il coinvolgimento degli
			elementi contigui alla mancanza, spesso sani. In alcuni casi le
			condizioni non permettono di ottenere protesi fisse, e si è costretti
			a ricorrere ad apparecchi 'mobili', che vanno rimossi e puliti dopo
			ogni pasto. L'implantologia consiste nel posizionamento chirurgico di
			strutture cilindriche in titanio all'interno dell'osso nelle sedi
			corrispondenti alle mancanze, che verranno usate come 'radici
			artificiali'.</p>

			<div class="imgs">
		<img src="/images/clinica/impianti/impianti.jpg" />
		</div>
		<p>Questo ci permette di non coinvolgere i denti contigui né con
			preparazioni che ne compromettano l'integrità né usandoli come
			appoggi per ganci o attacchi che molto spesso ne riducono la
			stabilità. Nella grande maggioranza dei casi, inoltre, si possono
			ottenere protesi fisse, che non devono essere rimosse e possono
			essere pulite come se fossero denti naturali.</p>

			<div class="imgs">
		<img src="/images/clinica/impianti/cer2.jpg" width="180" height="135" />
		<img src="/images/clinica/impianti/cer1.jpg" width="180" height="135" />
</div>
		<p>Gli impianti possono essere posizionati in qualunque momento: sia
			subito dopo l'estrazione che a distanza di anni, a patto che lo
			spessore residuo dell'osso sia sufficiente a garantire stabilità.
			Esistono comunque tecniche chirurgiche avanzate che consentono l'uso
			degli impianti in pressoché tutte le condizioni ossee. Il titanio è
			un metallo caratterizzato da un'alta tollerabilità, tanto che è
			comunemente usato nella produzione delle protesi ortopediche. Non
			esiste il rigetto, che è la reazione verso un tessuto organico
			biologicamente attivo, come avviene nei trapianti d'organo: il
			titanio non stimola alcuna reazione immunitaria da parte del tessuto
			ricevente, viene anzi 'osteointegrato', inglobato cioè dall'osso con
			un processo che richiede dai quattro ai sei mesi a seconda della sede
			di inserzione.</p>
