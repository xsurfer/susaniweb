		<p>Al termine di questo periodo di attesa, dopo una piccola anestesia,
			il chirurgo effettuerà una apertura sulla gengiva ed esporrà la testa
			dell'impianto sul quale verrà avvitato un moncone metallico come
			supporto per i vari tipi di protesi.</p>

