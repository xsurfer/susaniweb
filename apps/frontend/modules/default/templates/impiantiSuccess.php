		<p>Dopo dieci o quindici giorni la gengiva è guarita e gli impianti
			sono invisibili, al di sotto di essa. Così restano per tutto il
			periodo necessario all'integrazione: cinque mesi per l'arcata
			superiore e tre mesi per l'inferiore.</p>

			<div class="imgs">
		<img src="/images/clinica/impianti/foto/img07.jpg" width="200"
