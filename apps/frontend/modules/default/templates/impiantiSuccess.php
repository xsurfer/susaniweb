		<p>L'intervento chirurgico viene effettuato ambulatorialmente in
			anestesia locale ed è del tutto indolore. Il dolore postoperatorio è
			assente, tanto che la normale attività lavorativa può essere ripresa
			immediatamente o al massimo il giorno successivo.</p>
